            <h2>About High-Speed Rail in California</h2>
            <p>California has evaluated the potential for high-speed rail for several decades. It first pursued the idea
              of a Southern California high-speed rail corridor working with Japanese partners in 1981. In the
              mid-1990s, planning began in earnest as it became clear that California's growing population was putting
              an increasing strain on its highways, airports and conventional passenger rail lines. At the federal
              level, as part of the High-Speed Rail Development Act of 1994, authored by then-Representative Lynn
              Schenk, California was identified as one of the five corridors nationally for high-speed rail planning. In
              that same timeframe, the California Legislature created the Intercity High-Speed Rail Commission and
              charged it with determining the feasibility of a system in California. In 1996, the Commission issued a
              report that concluded that such a project was indeed feasible. That same year, the California High-Speed
              Rail Authority was created by the Legislature and was tasked with preparing a plan and design for the
              construction of a system to connect the state's major metropolitan areas.</p>
            <p>In 2002, following the release of the Authority's first business plan in 2000, Senate Bill (SB) 1856
              (Costa) was passed that authorized a $9.95 billion bond measure to finance the system. Submission of that
              measure to the state's voters was delayed several years. In the interim, the Authority, together with its
              federal partner, the Federal Railroad Administration (FRA), issued a Draft Program-Level Environmental
              Impact Report/Environmental Impact Statement (EIR/EIS) that described the system and its potential impacts
              on a statewide scale. Through that process, the Authority received and reviewed more than 2,000 public and
              government agency comments on the draft document, which was then used to determine the preferred corridors
              and stations for the system.</p>
            <p>In November 2008, the bond measure (Proposition 1A) was approved by the state's voters, making it the
              nation's first ever voter-approved financing mechanism for high-speed rail. In 2009, $8 billion in federal
              funds was made available nationwide as part of the American Recovery and Reinvestment Act (ARRA), which
              was passed to help stimulate the economy, create new jobs, and foster development of new rail
              manufacturing enterprises. This funding demonstrated a new commitment to the development of high-speed
              rail in the United States as embodied in a plan issued by the U.S. Department of Transportation: "A Vision
              of High-Speed Rail in America." California sought and successfully secured $3.3 billion in ARRA funds and
              other funds made available through federal appropriations and grants for planning and environmental work,
              as well as construction of the first construction section in the Central Valley.</p>
            <p>In 2012, Governor Edmund G. Brown, Jr. highlighted the benefits of this system in his State of the State
              address and declared that high-speed rail was a priority for his Administration, continuing his
              predecessor, Governor Schwarzenegger's, support for a high-speed rail system. Also in 2012, the Authority
              adopted its 2012 Business Plan that laid out a new framework for implementing the California high-speed
              rail system in concert with other state, regional and local rail investments, as part of a broader
              statewide rail modernization program. In that same year, the Legislature approved – and Governor Brown
              signed into law – SB 1029 (Budget Act of 2012) approving almost $8 billion in federal and state funds for
              the construction of the first high-speed rail investment in the Central Valley and 15 bookend and
              connectivity projects throughout the state. Work is underway on these major investments in California's
              transportation infrastructure.</p>
